public class Calculator {
    public double sum(double x, double y){
        return x+y;
    }
    public double multiply(double x, double y){
        return x*y;
    }
    //tema ex2
    public int subtracting(int x, int y){
        return x-y;
    }
    public double divisions (double x, double y){
        return x/y;
    }
    public double remainderDivisions(double x, double y){
        return x%y;
    }
}
